// No HTML escaping -- output goes into textarea.value (inherently safe)
// and into .md files on disk. HTML entities would corrupt the markdown.

export function generateAiPolicy(opts) {
  const optout = opts.training_optout === 'yes';

  let md = `# AI Policy

## Accountability

You are responsible for every line of code you submit, regardless of how it was produced. If you use AI tools, you must review, understand, and verify all output before committing it.

## AI Tool Usage

`;

  if (opts.ai_usage === 'permitted') {
    md += `AI tools are permitted for code generation, review, and documentation.\n\n`;
  } else if (opts.ai_usage === 'permitted-with-review') {
    md += `AI tools are permitted. AI-assisted contributions go through the same review process as everything else.\n\n`;
  } else if (opts.ai_usage === 'restricted') {
    md += `AI tool usage is restricted. Get maintainer approval before using AI tools on contributions.\n\n`;
  } else {
    md += `AI tools are not permitted for contributions to this project. All contributions must be human-authored.\n\n`;
  }

  md += `## AI-Generated Code\n\n`;

  if (opts.ai_code === 'accepted') {
    md += `AI-generated code is accepted. No special attribution or review process is required beyond the standard contribution workflow.\n\n`;
  } else if (opts.ai_code === 'requires-review') {
    md += `AI-generated code must be reviewed by a human maintainer before merging. Changes should be atomic and explainable -- you must be able to justify any line on request.\n\n`;
  } else if (opts.ai_code === 'requires-attribution') {
    md += `AI-generated code must be attributed. Use a commit trailer:\n\n\`\`\`text\nAssisted-by: <tool name>\n\`\`\`\n\nPull requests should note which portions were AI-generated.\n\n`;
  } else {
    md += `AI-generated code is not accepted. Pull requests identified as AI-generated will not be merged.\n\n`;
  }

  // Disclosure for presets that accept AI code with conditions
  if (opts.ai_usage !== 'permitted' && opts.ai_code !== 'accepted' && opts.ai_code !== 'not-accepted') {
    md += `## Disclosure\n\nIndicate AI involvement in commit messages using a trailer:\n\n\`\`\`text\nAssisted-by: <tool name>\n\`\`\`\n\n`;
  }

  // Prohibited uses for non-permissive presets
  if (opts.ai_usage !== 'permitted') {
    md += `## Prohibited Uses\n\n`;
    md += `- Bulk AI-generated PRs without meaningful review\n`;
    md += `- Submitting AI output you have not read and understood\n`;
    md += `- Using AI to generate code that circumvents tests or CI checks\n`;
    if (opts.ai_code === 'not-accepted') {
      md += `- Any AI-generated code contributions\n`;
    }
    md += `\n`;
  }

  md += `## AI in CI/CD\n\n`;

  if (opts.ai_cicd === 'permitted') {
    md += `AI tools may be used in CI/CD pipelines for code analysis, test generation, and deployment.\n\n`;
  } else if (opts.ai_cicd === 'restricted') {
    md += `AI tools in CI/CD pipelines require explicit maintainer approval.\n\n`;
  } else {
    md += `AI tools are not permitted in CI/CD pipelines. All automation must use deterministic, non-AI tooling.\n\n`;
  }

  md += `## Training Data\n\n`;

  if (optout) {
    md += `This project requests that its code not be used for AI model training. We ask that AI tool providers honor this preference.\n\n`;
  } else {
    md += `This project does not opt out of AI training data collection. Standard platform terms apply.\n\n`;
  }

  // Licensing -- only when AI code is accepted in some form
  if (opts.ai_code !== 'not-accepted') {
    md += `## Licensing\n\nAI-generated contributions must be compatible with this project's license. You must be able to certify your submissions under the project's contribution terms.\n\n`;
  }

  // Enforcement for non-permissive presets
  if (opts.ai_usage !== 'permitted') {
    md += `## Enforcement\n\nMaintainers may close AI-generated PRs that do not meet these standards without detailed feedback.\n\n`;
  }

  md += `## Policy Evolution\n\nThis policy will change as the tooling does. PRs welcome.\n`;

  md += FOOTER;
  return md;
}

export function generateAgents(opts) {
  const codeStyle = opts.code_style || '';
  const restrictedPaths = opts.restricted_paths || '';

  let md = `# Agents

Rules for AI coding agents working in this repository.

## General

- Follow existing code conventions and patterns
- Do not modify files outside the scope of the current task
- Prefer editing existing files over creating new ones
- Do not add dependencies without explicit approval
- Keep changes atomic -- one concern per commit
- Ask for clarification when requirements are ambiguous
`;

  if (codeStyle) {
    md += `\n## Code Style\n\n${codeStyle}\n`;
  }

  md += `
## Setup

> Add your project's setup commands here (npm install, go build, make, etc.)

## Testing

`;

  if (opts.test_reqs === 'none') {
    md += `Follow the project's existing testing conventions.\n`;
  } else if (opts.test_reqs === 'recommended') {
    md += `Write tests for new functionality. Run the full suite before submitting.\n`;
  } else {
    md += `Tests are required. Run the full test suite before submitting. PRs without adequate test coverage will not be merged.\n`;
  }

  if (restrictedPaths) {
    const paths = restrictedPaths.split(',').map(p => p.trim()).filter(Boolean);
    md += `\n## Restricted Paths\n\nDo not read or modify:\n\n`;
    for (const p of paths) {
      md += `- \`${p}\`\n`;
    }
  }

  md += `
## Commits

- Use clear, descriptive commit messages
- One logical change per commit
- Reference relevant issues when applicable
`;

  md += `\n## Review\n\n`;

  if (opts.review_reqs === 'none') {
    md += `No additional review requirements for AI-assisted changes.\n`;
  } else if (opts.review_reqs === 'significant-changes') {
    md += `Changes touching more than one module or any security-relevant code require human review. Minor fixes and doc updates follow the standard process.\n`;
  } else {
    md += `All pull requests require human review before merging, regardless of scope.\n`;
  }

  md += FOOTER;
  return md;
}

export function generateClaude(opts) {
  if (opts.claude_handling === 'symlink') {
    return `# CLAUDE.md

This file can be replaced with a symlink to AGENTS.md:

\`\`\`bash
ln -sf AGENTS.md CLAUDE.md
\`\`\`

All rules in AGENTS.md apply.
${FOOTER}`;
  }

  let md = `# CLAUDE.md

Instructions for Claude Code. All rules from AGENTS.md apply.

`;

  if (opts.test_reqs === 'required-before-merge') {
    md += `## Testing

Run the full test suite before proposing changes. Do not submit with failing tests.

`;
  }

  if (opts.restricted_paths) {
    md += `## Restricted

Do not read or modify files listed under Restricted Paths in AGENTS.md.

`;
  }

  md += `## Behavior

- Keep changes minimal and focused
- Verify your changes work before submitting
- When uncertain, ask rather than assume
`;

  md += FOOTER;
  return md;
}
